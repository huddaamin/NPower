package pkgswitch;

/**
 *
 * @author Amin Hudda
 */
public class Switch {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
      
        int user = 18;
      
      switch (user) {  
          case 18:
              System.out.println("you're 18");
              break;
          case 19:
              System.out.println("you're 19");
              break;
          case 20:
              System.out.println("you're 20");
              break;
          default:
              System.out.println("you're not 18, 19 or 20");
          
      }      
    }
    }     
    


